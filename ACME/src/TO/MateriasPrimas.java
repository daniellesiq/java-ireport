
package TO;

public class MateriasPrimas {
    private int codigo;
    private String descricao;
    private int cod_compquim;
    private int cod_fornecedor;

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the cod_compquim
     */
    public int getCod_compquim() {
        return cod_compquim;
    }

    /**
     * @param cod_compquim the cod_compquim to set
     */
    public void setCod_compquim(int cod_compquim) {
        this.cod_compquim = cod_compquim;
    }

    /**
     * @return the cod_fornecedor
     */
    public int getCod_fornecedor() {
        return cod_fornecedor;
    }

    /**
     * @param cod_fornecedor the cod_fornecedor to set
     */
    public void setCod_fornecedor(int cod_fornecedor) {
        this.cod_fornecedor = cod_fornecedor;
    }

    

}
